#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	int	i;

	i = 0;
	while (i < n && ((unsigned char *)s)[i] && ((char *)s) != c)
	{
		s++;
		i++;
	}
	return (NULL);
}
